package NextGenJavaByKamrul.Day_01;

public class day_1_PrimitiveVariables {
    public static void main(String[] args) {
        byte a = 100;
        short b = 20000;
        int c = 123456;
        long d = 1234567890L;
        float e = 10.5f;
        double f = 20.99;
        char g = 'A';
        boolean h = true;

        System.out.println("int: " + c + ", char: " + g + ", boolean: " + h);
    }
}
