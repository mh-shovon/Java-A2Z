package NextGenJavaByKamrul.Day_01;

public class day_1_While_Loop {
    public static void main(String[] args) {
        int i = 1;
        while(i <= 100){
            System.out.println("I is : " + i);
            i++;
        }

        int j = 1;
        do {
            System.out.println("J is : " + j);
            j+=1;
        } while(j<=100);
    }
}
